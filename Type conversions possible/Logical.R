# Logical
x <- TRUE

# Logical -> Numeric
as.numeric(x)

# Logical -> Integer
as.integer(x)

# Logical -> Logical
as.logical(x)

# Logical -> Character
as.character(x)

# Logical -> Complex
as.complex(x)

#-------------------------------------------

as.numeric(x)
# 1

as.integer(x)
# 1

as.character(x)
# "TRUE"

as.complex(x)
# 1+0i
