# Numeric
x <- 25.8

# Numeric -> Numeric
as.numeric(x)

# Numeric -> Integer
as.integer(x)

# Numeric -> Logical
as.logical(x)

# Numeric -> Character
as.character(x)

# Numeric -> Complex
as.complex(x)

#------------------------------------------


as.integer(x)
# 25

as.logical(x)
# TRUE

as.character(x)
# "25.8"

as.complex(x)
# 25.8+0i
