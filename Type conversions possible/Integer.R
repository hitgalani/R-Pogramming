# Integer
x <- 25L

# Integer -> Numeric
as.numeric(x)

# Integer -> Integer
as.integer(x)

# Integer -> Logical
as.logical(x)

# Integer -> Character
as.character(x)

# Integer -> Complex
as.complex(x)

#---------------------------------------

x <- 10L

as.numeric(x)
# 10

as.logical(x)
# TRUE

as.character(x)
# "10"

as.complex(x)
# 10+0i
